package com.stf.business.util;

public class PosWrapper {
	
	private String city;
	private String pos;
	private String productList;
	private String leadTime;
	private String arrivalPos;
	private Integer arrivalDays;
	public String getPos() {
		return pos;
	}
	public void setPos(String pos) {
		this.pos = pos;
	}
	public String getProductList() {
		return productList;
	}
	public void setProductList(String productList) {
		this.productList = productList;
	}
	public String getLeadTime() {
		return leadTime;
	}
	public void setLeadTime(String leadTime) {
		this.leadTime = leadTime;
	}
	public String getArrivalPos() {
		return arrivalPos;
	}
	public void setArrivalPos(String arrivalPos) {
		this.arrivalPos = arrivalPos;
	}
	public Integer getArrivalDays() {
		return arrivalDays;
	}
	public void setArrivalDays(Integer arrivalDays) {
		this.arrivalDays = arrivalDays;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	
	
	
	
	
	
	

}
